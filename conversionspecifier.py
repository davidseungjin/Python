# d is integer, f is floating-point decimal, s is string, x or X substitute as hexadecimal in lowercase or uppercase, e or E substitute as floating-point exponential format in lowercase or uppercase


print('%d' % 10)

print('%f' % 15.2)

print('%s' % 'ABCDE')

print('%X' % 31)

print('%x' % 31)

print('%E' % 15.2)

print('%e' % 15.2)

years = 15
total = 500 * (years * 0.02)

print('Savings after %d years is : %f' % (years, total))


